use anyhow::Context;
use glob::glob;

/// Extracts the SubjectPublicKeyInfo (SPKI) DER-encoded bytes from a
/// PEM-encoded certificate.
pub fn extract_spki_from_pem(pem: &str) -> anyhow::Result<Vec<u8>> {
    let pem_bytes = pem.as_bytes();
    // TODO: b/509861057 - Refactor unsafe Rust logic
    unsafe {
        let bio = bssl_sys::BIO_new_mem_buf(
            pem_bytes.as_ptr() as *const std::ffi::c_void,
            pem_bytes.len() as isize,
        );
        if bio.is_null() {
            anyhow::bail!("failed to create BIO");
        }

        let x509 =
            bssl_sys::PEM_read_bio_X509(bio, std::ptr::null_mut(), None, std::ptr::null_mut());
        bssl_sys::BIO_free(bio);

        if x509.is_null() {
            anyhow::bail!("failed to parse PEM certificate");
        }

        let pkey = bssl_sys::X509_get_pubkey(x509);
        bssl_sys::X509_free(x509);

        if pkey.is_null() {
            anyhow::bail!("failed to get public key from certificate");
        }

        let len = bssl_sys::i2d_PUBKEY(pkey, std::ptr::null_mut());
        if len <= 0 {
            bssl_sys::EVP_PKEY_free(pkey);
            anyhow::bail!("failed to determine public key DER length");
        }

        let mut der = vec![0u8; len as usize];
        let mut p = der.as_mut_ptr();
        let len2 = bssl_sys::i2d_PUBKEY(pkey, &mut p);
        bssl_sys::EVP_PKEY_free(pkey);

        if len2 <= 0 {
            anyhow::bail!("failed to serialize public key to DER");
        }

        Ok(der)
    }
}

/// Loads all certificates matching the specified glob pattern.
/// Returns a vector of strings, each containing the content of a certificate
/// file.
pub fn load_certificates(glob_pattern: &str) -> anyhow::Result<Vec<String>> {
    let mut certs = Vec::new();

    for entry in glob(glob_pattern).context("Failed to parse certificate glob pattern")? {
        let path = entry.context("Failed to resolve glob entry")?;
        if path.is_file() {
            let content = std::fs::read_to_string(&path)
                .with_context(|| format!("Failed to read certificate file {:?}", path))?;
            certs.push(content);
        }
    }
    Ok(certs)
}

